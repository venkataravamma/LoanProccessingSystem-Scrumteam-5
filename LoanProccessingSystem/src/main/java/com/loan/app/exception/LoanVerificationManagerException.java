package com.loan.app.exception;

public class LoanVerificationManagerException extends Exception {

	public LoanVerificationManagerException() {
		super();

	}

	public LoanVerificationManagerException(String message, Throwable cause) {
		super(message, cause);

	}

	public LoanVerificationManagerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public LoanVerificationManagerException(String message) {
		super(message);

	}

	public LoanVerificationManagerException(Throwable cause) {
		super(cause);

	}

}



